var min1=document.querySelector(`.minus1`);
var add1=document.querySelector(`.plus1`);
add1.addEventListener(`click`,()=>{
        var sp=document.querySelector(`#sp1`);
        var total=parseInt(sp.innerHTML)+1;
        sp.innerHTML=total;
});
min1.addEventListener(`click`,()=>{
        var sp=document.querySelector(`#sp1`);
        if(parseInt(sp.innerHTML)==0){
                sp.innerHTML=0;
        }
        else{
                var total=parseInt(sp.innerHTML)-1;
                sp.innerHTML=total;  
        }
});

